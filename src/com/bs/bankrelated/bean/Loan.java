package com.bs.bankrelated.bean;


public class Loan {
   private String loanId;
   private String loanType;
   private String dateOfBorrowing;
   private String loanAmount;
   private String interestRate;
   private String totalAmountHasToPay;
   private String noOfInstallment;
   private String loanPayementStartDate;
   private String installmentAmount;
   private String lastInstallmentDate;
   private String accountNo;
   private String loanStatus;
public String getLoanId() {
	return loanId;
}
public void setLoanId(String loanId) {
	this.loanId = loanId;
}
public String getLoanType() {
	return loanType;
}
public void setLoanType(String loanType) {
	this.loanType = loanType;
}
public String getDateOfBorrowing() {
	return dateOfBorrowing;
}
public void setDateOfBorrowing(String dateOfBorrowing) {
	this.dateOfBorrowing = dateOfBorrowing;
}
public String getLoanAmount() {
	return loanAmount;
}
public void setLoanAmount(String loanAmount) {
	this.loanAmount = loanAmount;
}
public String getInterestRate() {
	return interestRate;
}
public void setInterestRate(String interestRate) {
	this.interestRate = interestRate;
}
public String getTotalAmountHasToPay() {
	return totalAmountHasToPay;
}
public void setTotalAmountHasToPay(String totalAmountHasToPay) {
	this.totalAmountHasToPay = totalAmountHasToPay;
}
public String getNoOfInstallment() {
	return noOfInstallment;
}
public void setNoOfInstallment(String noOfInstallment) {
	this.noOfInstallment = noOfInstallment;
}
public String getLoanPayementStartDate() {
	return loanPayementStartDate;
}
public void setLoanPayementStartDate(String loanPayementStartDate) {
	this.loanPayementStartDate = loanPayementStartDate;
}
public String getInstallmentAmount() {
	return installmentAmount;
}
public void setInstallmentAmount(String installmentAmount) {
	this.installmentAmount = installmentAmount;
}
public String getLastInstallmentDate() {
	return lastInstallmentDate;
}
public void setLastInstallmentDate(String lastInstallmentDate) {
	this.lastInstallmentDate = lastInstallmentDate;
}
public String getAccountNo() {
	return accountNo;
}
public void setAccountNo(String accountNo) {
	this.accountNo = accountNo;
}
public String getLoanStatus() {
	return loanStatus;
}
public void setLoanStatus(String loanStatus) {
	this.loanStatus = loanStatus;
}
   
}
  