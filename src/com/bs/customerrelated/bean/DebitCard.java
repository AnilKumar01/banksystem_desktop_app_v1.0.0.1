package com.bs.customerrelated.bean;

public class DebitCard {
     private String debitCardNo;
     private String activationDate;
     private String accountNo;
     private String cardAccountLimit;
     private String expireDate;
     private String deactivationDate;
	public String getDebitCardNo() {
		return debitCardNo;
	}
	public void setDebitCardNo(String debitCardNo) {
		this.debitCardNo = debitCardNo;
	}
	public String getActivationDate() {
		return activationDate;
	}
	public void setActivationDate(String activationDate) {
		this.activationDate = activationDate;
	}
	public String getAccountNo() {
		return accountNo;
	}
	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}
	public String getCardAccountLimit() {
		return cardAccountLimit;
	}
	public void setCardAccountLimit(String cardAccountLimit) {
		this.cardAccountLimit = cardAccountLimit;
	}
	public String getExpireDate() {
		return expireDate;
	}
	public void setExpireDate(String expireDate) {
		this.expireDate = expireDate;
	}
	public String getDeactivationDate() {
		return deactivationDate;
	}
	public void setDeactivationDate(String deactivationDate) {
		this.deactivationDate = deactivationDate;
	}
     
}
